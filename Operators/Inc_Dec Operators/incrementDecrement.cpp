#include <iostream>

using namespace std;

int main()
{
    int i;
    cout << "Enter the value of i : ";
    cin >> i;
    cout << "The Value Of ++i is : " << ++i << endl;
    cout << "The Value Of i++ is : " << i++ << endl;
    cout << "The Value Of i-- is : " << i-- << endl;
    cout << "The Value Of --i is : " << --i << endl;
    return 0;
}